current_message = input().split(" | ")
MORSE_CODE_DICT = {'..-.': 'F', '-..-': 'X',
                   '.--.': 'P', '-': 'T', '..---': '2',
                   '....-': '4', '-----': '0', '--...': '7',
                   '...-': 'V', '-.-.': 'C', '.': 'E', '.---': 'J',
                   '---': 'O', '-.-': 'K', '----.': '9', '..': 'I',
                   '.-..': 'L', '.....': '5', '...--': '3', '-.--': 'Y',
                   '-....': '6', '.--': 'W', '....': 'H', '-.': 'N', '.-.': 'R',
                   '-...': 'B', '---..': '8', '--..': 'Z', '-..': 'D', '--.-': 'Q',
                   '--.': 'G', '--': 'M', '..-': 'U', '.-': 'A', '...': 'S', '.----': '1'}
new_message = ''
for letter in current_message:
    for ltr in letter.split(" "):
        if ltr != '':
            new_message += MORSE_CODE_DICT[ltr]
    new_message += ' '
print(new_message)
